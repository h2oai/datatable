static void parse_naive(ParseContext& ctx) {
  const char* ch = ctx.ch;
  double r = 0.0;
  uint_fast8_t digit;
  bool neg = false;

  if (*ch == '-') {
    neg = true;
    ++ch;
  }
  while ((digit = (uint_fast8_t)(*ch - '0')) < 10) {
    r = (r*10.0) + digit;
    ++ch;
  }
  if (*ch == '.') {
    double f = 0.0;
    int n = 0;
    ++ch;
    while (*ch >= '0' && *ch <= '9') {
        f = (f*10.0) + (*ch - '0');
        ++ch;
        ++n;
    }
    r += f / std::pow(10.0, n);
  }
  if (neg) {
    r = -r;
  }
  ctx.target->float64 = r;
  ctx.ch = ch;
}

